package com.javarush.task.task40.task4009;

import java.time.LocalDate;
import java.time.Year;
import java.time.format.DateTimeFormatter;
import java.time.temporal.ChronoUnit;
import java.time.temporal.TemporalField;
import java.util.Locale;

/* 
Buon Compleanno!
*/

public class Solution {
    public static void main(String[] args) {
        System.out.println(getWeekdayOfBirthday("30.05.1984", "2015"));
        System.out.println(getWeekdayOfBirthday("1.12.2015", "2016"));
    }

    public static String getWeekdayOfBirthday(String birthday, String year) {
        LocalDate bd = LocalDate.parse(birthday, DateTimeFormatter.ofPattern("d.M.yyyy"));
        Year y = Year.parse(year);
        LocalDate newDate = bd.withYear(y.getValue());

        return newDate.format(DateTimeFormatter.ofPattern("EEEE", Locale.ITALIAN));
    }
}
